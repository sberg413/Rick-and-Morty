package com.sberg413.rickandmorty.adapters

import android.view.LayoutInflater
import android.view.ViewGroup
import androidx.core.view.isVisible
import androidx.paging.LoadState
import androidx.paging.LoadStateAdapter
import androidx.recyclerview.widget.RecyclerView
import com.sberg413.rickandmorty.databinding.LoadStateFooterItemBinding

class CharacterLoadStateAdapter(private val retry: () -> Unit)
    : LoadStateAdapter<CharacterLoadStateAdapter.LoadStateViewHolder>() {

    override fun onBindViewHolder(holder: LoadStateViewHolder, loadState: LoadState) {
        holder.bind(loadState)
    }

    override fun onCreateViewHolder(parent: ViewGroup, loadState: LoadState): LoadStateViewHolder {
        val loadStateFooterItemBinding = LoadStateFooterItemBinding
            .inflate(LayoutInflater.from(parent.context), parent, false)
        return LoadStateViewHolder(loadStateFooterItemBinding, retry)
    }


    class LoadStateViewHolder (
        private val binding: LoadStateFooterItemBinding,
        retry: () -> Unit
    ) : RecyclerView.ViewHolder(binding.root) {

        init {
            binding.retryButton.setOnClickListener { retry.invoke() }
        }

        fun bind(loadState: LoadState) {
            if (loadState is LoadState.Error) {
                binding.errorMsg.text = loadState.error.localizedMessage
            }
            binding.progressBar.isVisible = loadState is LoadState.Loading
            binding.retryButton.isVisible = loadState is LoadState.Error
            binding.errorMsg.isVisible = loadState is LoadState.Error
        }
    }
}